    [ "$goteof" = "y" ] && return;
    [ "$gotline" != "y" ] && {
        if read -r line
        then
            gotline=y
        else
            goteof=y
            return
        fi
    }
    [ "$line" = "" ] && {
        gotline=n
        ((M[P]=10))
        return
    }
    if ((BASH_VERSINFO[0]>0)) ; then
        c="${line:0:1}"
        line="${line:1}"
        printf -v C %d "'$c"
    
    elif ( line=nnynnn; j=2; [[ "${line:j:1}" == y ]] ) 2>/dev/null ; then
        c="${line:0:1}"
        line="${line:1}"
        C=$(printf %d "'$c")
    
    else
        C="$line"
        while [ ${#C} -gt 1 ] ; do C="${C%?}"; done
        line="${line#?}"
        C=`echo "$C" |dd bs=1 count=1 2>/dev/null |od -d |awk 'NR==1{print 0+$2;}'`
    fi
    ((M[P]=C))
}

